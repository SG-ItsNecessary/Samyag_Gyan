// ==================== SAVE MINDMAP FUNCTIONALITY ====================
function saveMindMap() {
  const { src, lang, title, publishDate } = currentMindMapData;

  // Convert date to DD-MM-YYYY format
  const dateParts = publishDate.trim().split(' '); // "18 July 2025"
  const day = dateParts[0].padStart(2, '0');
  const monthNames = {
    'january': '01', 'february': '02', 'march': '03', 'april': '04',
    'may': '05', 'june': '06', 'july': '07', 'august': '08',
    'september': '09', 'october': '10', 'november': '11', 'december': '12'
  };
  const month = monthNames[dateParts[1].toLowerCase()];
  const year = dateParts[2];
  const formattedDate = `${day}-${month}-${year}`;

  // Clean title (replace spaces with underscores, remove special chars)
  const cleanTitle = title.replace(/[^a-zA-Z0-9\s\u0900-\u097F]/g, '').replace(/\s+/g, '_');

  // Create filename
  const langSuffix = lang === 'hi' ? 'hi' : 'en';
  const filename = `${formattedDate}_${cleanTitle}_${langSuffix}.png`;

  // Create download link
  const link = document.createElement('a');
  link.href = src;
  link.download = filename;
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
}

