function updateURLForArticle(title, publishDate, articleType = 'news') {
  const slug = slugifyTitle(title);
  // Convert "18 July 2025" to "2025-07-18" for clean, SEO-friendly URLs
  const isoDate = convertDateToISO(publishDate);

  // SEO-optimized URL structure based on article type (v2.0)
  let newPath;
  switch(articleType) {
    case 'editorial':
      newPath = `/upsc-editorials/${isoDate}/${slug}`;  // ‚úÖ Updated: cleaner URL
      break;
    case 'ethics':
      newPath = `/upsc-ethics/${slug}`;  // ‚úÖ Updated: NO DATE (timeless content)
      break;
    case 'essay':
      newPath = `/upsc-essays/${slug}`;  // ‚úÖ Updated: NO DATE (timeless content) + plural
      break;
    case 'news':
    default:
      newPath = `/upsc-current-affairs/${isoDate}/${slug}`;
      break;
  }

  history.replaceState(null, "", newPath);
}

